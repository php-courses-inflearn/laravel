<?php

/**
 * Replace
 */

str_replace('Hello', 'Hi', 'Hello, world');

/*
 * Repeat
 */
str_repeat('*', 5);

/*
 * Split
 */
str_split('Hello, world', 3);

/*
 * Word count
 */
str_word_count('Hello, world', 2);

/*
 * Compare
 */
strcmp('Hello, world', 'Hello, world');

/*
 * Position
 */
strpos('Hello, world', 'o');

/*
 * First Position
 */
strpbrk('Hello, world', 'o');

/*
 * Length
 */
strlen('Hello, world');

/*
 * Reverse
 */
strrev('Hello, world');
